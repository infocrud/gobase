package main

import (
	"encoding/json"
	"fmt"
	"io"
	"net/http"
	"os"
	"os/exec"
	"path/filepath"
	"strings"
	"time"
)

const version = "0.1.0"

func main() {
	if len(os.Args) < 2 {
		printUsage()
		os.Exit(0)
	}

	switch os.Args[1] {
	case "init":
		runInit()
	case "start":
		runStart()
	case "stop":
		runStop()
	case "status":
		runStatus()
	case "logs":
		service := ""
		if len(os.Args) >= 3 {
			service = os.Args[2]
		}
		runLogs(service)
	case "migrate":
		runMigrate()
	case "deploy":
		runDeploy()
	case "version", "--version", "-v":
		fmt.Println("gobase", version)
	case "help", "--help", "-h":
		printUsage()
	default:
		fmt.Fprintf(os.Stderr, "unknown command: %s\n\n", os.Args[1])
		printUsage()
		os.Exit(1)
	}
}

func printUsage() {
	fmt.Print(`GoBase CLI ` + version + ` — open-source BaaS

Usage:
  gobase <command> [flags]

Commands:
  init        Create .env and docker-compose.yml in the current directory
  start       Start the GoBase stack (docker compose up -d)
  stop        Stop the GoBase stack (docker compose down)
  status      Show health of each running service
  logs [svc]  Tail logs for all services, or a specific one
  migrate     Run database migrations against the running stack
  deploy      Deploy to GoBase Cloud (requires GOBASE_TOKEN env var)
  version     Print version

Examples:
  gobase init
  gobase start
  gobase status
  gobase logs auth
  gobase migrate
`)
}

